#==============================================================================
# 設定項目
#==============================================================================
module Kazari
module DiscriminateEnemy
  # この数値(％)の差が生まれます。
  # 例：最大HPが10000の場合、この数値が 5 だったら、
  # 9500～10500になります。（±500[10000 x 5 %]のずれが生まれる）
  PER = 20
end
end
#==============================================================================
# ここまで
#==============================================================================

$kzr_imported = {} if $kzr_imported == nil
$kzr_imported["DiscriminateEnemy"] = true
#==============================================================================
# ■ Game_Enemy
#==============================================================================
class Game_Enemy < Game_Battler
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  alias kzr_dis_enemy_initialize initialize
  def initialize(index, enemy_id)
    kzr_dis_enemy_initialize(index, enemy_id)
    8.times {|i| kzr_set_param_plus(i) }
  end
  #--------------------------------------------------------------------------
  # ● 差別値の設定
  #--------------------------------------------------------------------------
  def kzr_set_param_plus(i)
    amp = param(i) * Kazari::DiscriminateEnemy::PER / 100
    @param_plus[i] += rand(amp + 1) + rand(amp + 1) - amp
  end
end